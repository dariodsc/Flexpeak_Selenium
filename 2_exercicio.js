// Função que faz as 4 operações matématicas

function calculo (num_1, num_2){
    console.log(num_1, "+" ,num_2, " = ", num_1 + num_2) // Aqui irá mostrar os números a serem somados e seu resultador
    console.log(num_1, "-" ,num_2, " = ", num_1 - num_2)
    console.log(num_1, "*" ,num_2, " = ", num_1 * num_2)
    console.log(num_1, "/" ,num_2, " = ", num_1 / num_2)
}

calculo(10, 5)                                            // Aqui declaramos os números que serão usados durante execução
